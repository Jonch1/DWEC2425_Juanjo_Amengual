//Estructura de control switch

let dia = prompt("Escribe el dia que quieres saber el horario (DL, DM, DM, DJ, DV, DS, DG)");

switch(dia) {
    case (dia= "DL"):
        alert ("Apertura a las 5:00");
        break;


    case (dia= "DM"):
        alert ("Apertura a las 6:00");
        break;


    case (dia= "DX"):
        alert ("Apertura a las 7:00");
        break;


    case (dia= "DJ"):
        alert ("Apertura a las 8:00");
        break;


    case (dia= "DV"):
        alert ("Apertura a las 9:00");
        break;


    case (dia= "DS"):
        alert ("Apertura a las 10:00");
        break;


    case (dia= "DG"):
        alert ("Apertura a las 11:00");
        break;
}